# .506 logo doctor -- stdlib PNG surgery for user brand art.
#
# On-box: the splash showed a boxy panel around the logo. The launcher renders
# real PNG alpha correctly and even matches the splash background to the
# logo's corner pixel -- a visible panel means the PNG itself carries an
# OPAQUE background (classically, an export that rendered the transparency
# checkerboard into pixels; the old make_icons.py preserves alpha faithfully
# but cannot remove a background that is baked into the source). These
# helpers inspect a PNG and strip such a background: every background pixel
# CONNECTED TO THE BORDER becomes transparent (an edge flood), so whites
# INSIDE the glyph survive. Pure stdlib (zlib/struct), 8-bit non-interlaced
# PNGs (gray / RGB / palette / RGBA) -- everything a logo export produces.
# CLI:  python -m samql_core._brand inspect  logo.png
#       python -m samql_core._brand fix      logo.png out.png [--tol N]
#                [--color RRGGBB[,RRGGBB...]] [--flatten RRGGBB] [--resize N]
# --------------------------------------------------------------------------
import struct as _struct
import zlib as _zlib

_PNG_SIG = b"\x89PNG\r\n\x1a\n"


def png_decode(data):
    """Decode an 8-bit non-interlaced PNG to (w, h, rows) where rows is a
    list of bytearrays of RGBA (4 bytes per pixel). Raises ValueError with a
    plain-English reason for unsupported variants."""
    if data[:8] != _PNG_SIG:
        raise ValueError("not a PNG file")
    pos, w = 8, None
    h = bitd = ctype = None
    idat, plte, trns = bytearray(), b"", b""
    while pos < len(data):
        ln, typ = _struct.unpack(">I4s", data[pos:pos + 8])
        chunk = data[pos + 8:pos + 8 + ln]
        pos += 12 + ln
        if typ == b"IHDR":
            w, h, bitd, ctype, _c, _f, inter = _struct.unpack(
                ">IIBBBBB", chunk)
            if bitd != 8:
                raise ValueError("only 8-bit PNGs supported (got %d-bit); "
                                 "re-export at 8 bits/channel" % bitd)
            if inter != 0:
                raise ValueError("interlaced PNG not supported; re-export "
                                 "non-interlaced")
            if ctype not in (0, 2, 3, 6):
                raise ValueError("unsupported PNG color type %d" % ctype)
        elif typ == b"PLTE":
            plte = chunk
        elif typ == b"tRNS":
            trns = chunk
        elif typ == b"IDAT":
            idat += chunk
        elif typ == b"IEND":
            break
    raw = _zlib.decompress(bytes(idat))
    ch = {0: 1, 2: 3, 3: 1, 6: 4}[ctype]
    stride = w * ch
    rows, prev = [], bytearray(stride)
    p = 0
    for _y in range(h):
        f = raw[p]
        line = bytearray(raw[p + 1:p + 1 + stride])
        p += 1 + stride
        if f == 1:                              # Sub
            for i in range(ch, stride):
                line[i] = (line[i] + line[i - ch]) & 0xFF
        elif f == 2:                            # Up
            for i in range(stride):
                line[i] = (line[i] + prev[i]) & 0xFF
        elif f == 3:                            # Average
            for i in range(stride):
                a = line[i - ch] if i >= ch else 0
                line[i] = (line[i] + ((a + prev[i]) >> 1)) & 0xFF
        elif f == 4:                            # Paeth
            for i in range(stride):
                a = line[i - ch] if i >= ch else 0
                b = prev[i]
                c = prev[i - ch] if i >= ch else 0
                pa, pb, pc = abs(b - c), abs(a - c), abs(a + b - 2 * c)
                pr = a if (pa <= pb and pa <= pc) else (b if pb <= pc else c)
                line[i] = (line[i] + pr) & 0xFF
        elif f != 0:
            raise ValueError("bad PNG filter byte %d" % f)
        prev = line
        out = bytearray(w * 4)
        if ctype == 6:
            out[:] = line
        elif ctype == 2:
            for x in range(w):
                out[4 * x:4 * x + 3] = line[3 * x:3 * x + 3]
                out[4 * x + 3] = 255
        elif ctype == 0:
            for x in range(w):
                g = line[x]
                out[4 * x:4 * x + 4] = bytes((g, g, g, 255))
        else:                                   # palette
            for x in range(w):
                ix = line[x]
                out[4 * x:4 * x + 3] = plte[3 * ix:3 * ix + 3]
                out[4 * x + 3] = trns[ix] if ix < len(trns) else 255
        rows.append(out)
    return w, h, rows


def png_encode(w, h, rows):
    """Encode RGBA rows (as from png_decode) to PNG bytes."""
    raw = bytearray()
    for r in rows:
        raw.append(0)
        raw += r
    comp = _zlib.compress(bytes(raw), 9)

    def chunk(typ, payload):
        return (_struct.pack(">I", len(payload)) + typ + payload
                + _struct.pack(">I", _zlib.crc32(typ + payload) & 0xFFFFFFFF))
    return (_PNG_SIG
            + chunk(b"IHDR", _struct.pack(">IIBBBBB", w, h, 8, 6, 0, 0, 0))
            + chunk(b"IDAT", comp) + chunk(b"IEND", b""))


def _border_colors(w, h, rows, min_share=0.02):
    """Distinct opaque colors on the 1px border holding >= min_share of it --
    the background candidates (both tiles of a baked checkerboard qualify;
    a glyph edge touching the border does not)."""
    counts = {}
    total = 0
    for x in range(w):
        for y in (0, h - 1):
            px = rows[y][4 * x:4 * x + 4]
            if px[3]:
                counts[bytes(px[:3])] = counts.get(bytes(px[:3]), 0) + 1
            total += 1
    for y in range(1, h - 1):
        for x in (0, w - 1):
            px = rows[y][4 * x:4 * x + 4]
            if px[3]:
                counts[bytes(px[:3])] = counts.get(bytes(px[:3]), 0) + 1
            total += 2
    return [c for c, n in sorted(counts.items(), key=lambda kv: -kv[1])
            if n >= max(1, int(total * min_share))]


def logo_inspect(data):
    """Report what the launcher will see: size, real alpha or not, and the
    border's background candidates (2+ light border colors = the classic
    baked-in transparency checkerboard)."""
    w, h, rows = png_decode(data)
    has_alpha = any(r[i] != 255 for r in rows for i in range(3, len(r), 4))
    bc = _border_colors(w, h, rows)
    checker = (len(bc) == 2
               and all(min(c) > 170 for c in bc)
               and max(abs(bc[0][i] - bc[1][i]) for i in range(3)) < 64)
    return {"width": w, "height": h, "has_alpha": has_alpha,
            "border_colors": ["#%02x%02x%02x" % tuple(c) for c in bc],
            "checkerboard_suspected": bool(checker and not has_alpha)}


def logo_make_transparent(data, colors=None, tol=24):
    """Strip a baked-in background: flood from the border through every pixel
    within ``tol`` per channel of a background color (default: the border's
    own dominant colors -- covers a solid matte AND a baked checkerboard),
    setting alpha 0. Interior pixels of the same color survive: only
    border-CONNECTED background is cleared."""
    w, h, rows = png_decode(data)
    targets = ([bytes.fromhex(c.lstrip("#")) for c in colors] if colors
               else [bytes(c) for c in _border_colors(w, h, rows)])
    if not targets:
        return png_encode(w, h, rows)

    def is_bg(x, y):
        px = rows[y][4 * x:4 * x + 4]
        if not px[3]:
            return False
        return any(all(abs(px[i] - t[i]) <= tol for i in range(3))
                   for t in targets)
    seen = bytearray(w * h)
    stack = [(x, y) for x in range(w) for y in (0, h - 1) if is_bg(x, y)]
    stack += [(x, y) for y in range(1, h - 1) for x in (0, w - 1)
              if is_bg(x, y)]
    for x, y in stack:
        seen[y * w + x] = 1
    while stack:
        x, y = stack.pop()
        rows[y][4 * x + 3] = 0
        for nx, ny in ((x - 1, y), (x + 1, y), (x, y - 1), (x, y + 1)):
            if 0 <= nx < w and 0 <= ny < h and not seen[ny * w + nx] \
                    and is_bg(nx, ny):
                seen[ny * w + nx] = 1
                stack.append((nx, ny))
    return png_encode(w, h, rows)


def logo_flatten(data, color):
    """Composite the PNG's alpha onto a solid color (for a deliberately
    opaque splash art that matches a chosen background exactly)."""
    w, h, rows = png_decode(data)
    c = bytes.fromhex(color.lstrip("#"))
    for r in rows:
        for x in range(0, len(r), 4):
            a = r[x + 3]
            if a == 255:
                continue
            for i in range(3):
                r[x + i] = (r[x + i] * a + c[i] * (255 - a)) // 255
            r[x + 3] = 255
    return png_encode(w, h, rows)


def logo_resize(data, max_side):
    """Nearest-neighbor downscale so the longer side == max_side (logos only;
    exact color edges stay crisp, no resample halo to re-pollute the alpha)."""
    w, h, rows = png_decode(data)
    if max(w, h) <= max_side:
        return png_encode(w, h, rows)
    scale = max_side / float(max(w, h))
    nw, nh = max(1, int(w * scale)), max(1, int(h * scale))
    out = []
    for ny in range(nh):
        sy = min(h - 1, int(ny / scale))
        row = bytearray(nw * 4)
        for nx in range(nw):
            sx = min(w - 1, int(nx / scale))
            row[4 * nx:4 * nx + 4] = rows[sy][4 * sx:4 * sx + 4]
        out.append(row)
    return png_encode(nw, nh, out)


_PUBLIC_LOGO_NAMES = ("logo.png", "app-icon.png", "apple-touch-icon.png")


def logo_fix_file(path, *, tol=24, colors=None):
    """Strip a baked-in background from a PNG on disk (in place).

    Returns ``{path, before, after, changed}``. Used by ``build.ps1`` /
    ``build.sh`` so splash / tab icons ship with real alpha instead of a
    boxy matte. No-op (``changed=False``) when the file is already clean or
    missing.
    """
    import os as _os
    if not path or not _os.path.isfile(path):
        return {"path": path, "before": None, "after": None, "changed": False}
    with open(path, "rb") as fh:
        data = fh.read()
    before = logo_inspect(data)
    fixed = logo_make_transparent(data, colors=colors, tol=tol)
    after = logo_inspect(fixed)
    changed = fixed != data
    if changed:
        # Atomic-ish replace so a killed build never leaves a half-written PNG.
        tmp = path + ".samql-tmp"
        with open(tmp, "wb") as fh:
            fh.write(fixed)
        _os.replace(tmp, path)
    return {"path": path, "before": before, "after": after, "changed": changed}


def logo_fix_public_dir(public_dir, *, tol=24):
    """Fix every known brand PNG under ``frontend/public`` (or any folder)."""
    import os as _os
    results = []
    for name in _PUBLIC_LOGO_NAMES:
        results.append(
            logo_fix_file(_os.path.join(public_dir, name), tol=tol))
    return results


def ensure_public_brand_pngs(public_dir):
    """Write the embedded SQ mark into ``frontend/public`` when drop-ins are absent.

    Binary brand PNGs are not in the text-only SOURCE_MANIFEST, so a fresh
    expand / checkout leaves ``frontend/public`` empty. The HTTP server already
    falls back to ``app_icon_png()`` for ``/logo.png``, but the launcher splash
    and Vite ``public/`` copy need real files on disk. Never overwrites a
    user-supplied PNG. Returns a list of
    ``{path, name, written}`` dicts.
    """
    import os as _os
    out = []
    if not public_dir:
        return out
    try:
        _os.makedirs(public_dir, exist_ok=True)
    except OSError:
        return out
    data = app_icon_png()
    for name in _PUBLIC_LOGO_NAMES:
        path = _os.path.join(public_dir, name)
        written = False
        if not _os.path.isfile(path):
            tmp = path + ".samql-tmp"
            try:
                with open(tmp, "wb") as fh:
                    fh.write(data)
                _os.replace(tmp, path)
                written = True
            except OSError:
                try:
                    if _os.path.isfile(tmp):
                        _os.remove(tmp)
                except OSError:
                    pass
        out.append({"path": path, "name": name, "written": written})
    return out


def _brand_cli(argv):
    import json as _json
    if len(argv) >= 2 and argv[0] == "inspect":
        with open(argv[1], "rb") as fh:
            print(_json.dumps(logo_inspect(fh.read()), indent=2))
        return 0
    if len(argv) >= 2 and argv[0] == "ensure-public":
        print(_json.dumps(ensure_public_brand_pngs(argv[1]), indent=2))
        return 0
    if len(argv) >= 2 and argv[0] == "fix-public":
        # build.ps1 / build.sh: strip opaque mattes from public brand PNGs
        # before the frontend (and therefore the frozen splash) is packaged.
        tol = 24
        i = 2
        while i < len(argv):
            if argv[i] == "--tol":
                tol = int(argv[i + 1]); i += 2
            else:
                i += 1
        results = logo_fix_public_dir(argv[1], tol=tol)
        print(_json.dumps(results, indent=2))
        return 0
    if len(argv) >= 3 and argv[0] == "fix":
        with open(argv[1], "rb") as fh:
            data = fh.read()
        colors, tol, flat, rez = None, 24, None, None
        i = 3
        while i < len(argv):
            if argv[i] == "--tol":
                tol = int(argv[i + 1]); i += 2
            elif argv[i] == "--color":
                colors = argv[i + 1].split(","); i += 2
            elif argv[i] == "--flatten":
                flat = argv[i + 1]; i += 2
            elif argv[i] == "--resize":
                rez = int(argv[i + 1]); i += 2
            else:
                i += 1
        if flat:
            data = logo_flatten(data, flat)
        else:
            data = logo_make_transparent(data, colors=colors, tol=tol)
        if rez:
            data = logo_resize(data, rez)
        with open(argv[2], "wb") as fh:
            fh.write(data)
        print("wrote %s -- %s" % (argv[2], _json.dumps(logo_inspect(data))))
        return 0
    print("usage: python -m samql_core._brand inspect LOGO.png\n"
          "       python -m samql_core._brand fix LOGO.png OUT.png "
          "[--tol N] [--color RRGGBB[,RRGGBB]] [--flatten RRGGBB] "
          "[--resize N]\n"
          "       python -m samql_core._brand ensure-public DIR\n"
          "       python -m samql_core._brand fix-public DIR [--tol N]")
    return 2


